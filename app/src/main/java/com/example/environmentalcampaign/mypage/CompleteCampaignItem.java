package com.example.environmentalcampaign.mypage;

public class CompleteCampaignItem {

    private String campaignCode;
    private double achievementAvg;
    private int reCount;

    public CompleteCampaignItem(){}

    public String getCampaignCode() { return campaignCode; }

    public void setCampaignCode(String campaignCode) { this.campaignCode = campaignCode; }

    public double getAchievementAvg() { return achievementAvg; }

    public void setAchievementAvg(double achievementAvg) { this.achievementAvg = achievementAvg; }

    public int getReCount() { return reCount; }

    public void setReCount(int reCount) { this.reCount = reCount; }
}
