package com.example.environmentalcampaign.certification_page;

public class Certi_Info {
    private String publisher;
    private String certi_date;
    private String contents;
    private String photoUrl;

    public Certi_Info() { }

    public String getPublisher() {
        return publisher;
    }

    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }

    public String getCerti_date() {
        return certi_date;
    }

    public void setCerti_date(String certi_date) {
        this.certi_date = certi_date;
    }

    public String getContents() {
        return contents;
    }

    public void setContents(String contents) {
        this.contents = contents;
    }

    public String getPhotoUrl() {
        return photoUrl;
    }

    public void setPhotoUrl(String photoUrl) {
        this.photoUrl = photoUrl;
    }
}
